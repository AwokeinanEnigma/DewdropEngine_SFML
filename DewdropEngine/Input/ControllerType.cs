/// <summary>
/// Represents the type of controller used for user input.
/// </summary>
public enum ControllerType {
	/// <summary>
	/// Represents keyboard input.
	/// </summary>
	Keyboard,

	/// <summary>
	/// Represents input from an Xbox 360 controller.
	/// </summary>
	Xbox360,

	/// <summary>
	/// Represents input from a Playstation 4 controller.
	/// </summary>
	Playstation4,

	/// <summary>
	/// Represents input from a Nintendo Switch controller.
	/// </summary>
	Switch
}
